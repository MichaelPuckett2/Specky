namespace Specky.Enums
{
    /// <summary>
    /// The role of the Speck.
    /// </summary>
    public enum Role
    {
        /// <summary>
        /// The Speck has the role of a normal Type.
        /// </summary>
        Type,
        /// <summary>
        /// The Speck takes on the role of a service.
        /// </summary>
        Service,
        /// <summary>
        /// The Speck takes on the role of a set of data.
        /// </summary>
        DataSet
    }
}
